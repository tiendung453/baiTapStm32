void set_pid()
{
	// pi dieu chinh target angle
	encoder_err = xung;
	xung = 0;
	encoder_lowout = (1 - he_so_lowfilter)* encoder_err+ he_so_lowfilter* encoder_lowout;
	encoder_sum += encoder_lowout;
	encoder_sum = constrain(encoder_sum, -500, 500);
	AngFix = Velocity_kp*encoder_lowout + Velocity_ki*encoder_sum;
	
	// pd dieu chinh goc
	gyro_lowout = (1-he_so_lowfilter)*gyrox + he_so_lowfilter*gyro_lowout;
	target_angle = -goc_can_bang + AngFix;
	error = AngX - target_angle;
	derivative = gyro_lowout;
	pwm_Out = Vertical_kp * error + Vertical_kd * derivative;
	pwm_Out = constrain(pwm_Out, -399, 399);
	
	if(pwm_Out > 0) 
	{
		counter_clock_wise();
		Motor_SetPWM(&htim3, TIM_CHANNEL_1,399-pwm_Out);
	}
	else if(pwm_Out <0)
	{
		clock_wise();
		Motor_SetPWM(&htim3, TIM_CHANNEL_1,399+pwm_Out);
	}
	else Motor_SetPWM(&htim3, TIM_CHANNEL_1,399);
}

void calib_gyro()
{
	int samples = 150;
	float sumX = 0;
	uint16_t last_time;
	for(int i = 0; i<samples; i++)
	{
		MPU6050_Read_Gyro(&hi2c1, &MPU6050);
		sumX += MPU6050.Gx;
		last_time = HAL_GetTick();
		while((HAL_GetTick() - last_time) < 5);
	}
	
	gyrox_offset = sumX/samples;
}


